# CyberForge Color Definitions

class C:
    R = '\033[91m'    # Red
    G = '\033[92m'    # Green
    Y = '\033[93m'    # Yellow
    B = '\033[94m'    # Blue
    M = '\033[95m'    # Magenta
    C = '\033[96m'    # Cyan
    W = '\033[97m'    # White
    BD = '\033[1m'    # Bold
    DIM = '\033[2m'   # Dim
    END = '\033[0m'   # Reset
